// === APLICA TRADUÇÕES E DISPARA EVENTO ===
function applyTranslations(lang) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.documentElement.lang = lang;
  localStorage.setItem("casa-lang", lang);

  // Dispara evento global para atualizar quartos
  const event = new CustomEvent("langChanged", { detail: { lang } });
  document.dispatchEvent(event);
}

function translateElfsight(lang) {
  const t = ELFSIGHT_TRANSLATIONS[lang] || ELFSIGHT_TRANSLATIONS["pt"];

  // Esperar o widget carregar via MutationObserver
  const observer = new MutationObserver(() => {
    const widget = document.querySelector(".elfsight-app"); // wrapper
    if (!widget) return;

    // TÍTULO
    const titleEl = widget.querySelector("h2, h3, .es-widget-title");
    if (titleEl) titleEl.innerText = t.title;

    // BOTÃO DE REVIEW
    const buttonEl = widget.querySelector("a, button");
    if (buttonEl && buttonEl.innerText.toLowerCase().includes("google")) {
      buttonEl.innerText = t.button;
    }
  });

  // Observar alterações no DOM
  observer.observe(document.body, { childList: true, subtree: true });
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("casa-lang");
  const browserLang = navigator.language.toLowerCase().startsWith("en")
    ? "en"
    : navigator.language.toLowerCase().startsWith("fr")
    ? "fr"
    : navigator.language.toLowerCase().startsWith("es")
    ? "es"
    : "pt";
  const langToUse = saved || browserLang;
  applyTranslations(langToUse);
  translateElfsight(langToUse);

  // Atualiza dropdown ao mudar idioma
  const langOptions = document.querySelectorAll(".lang-option");
  langOptions.forEach((option) => {
    option.addEventListener("click", () => {
      const selectedLang = option.getAttribute("data-lang");
      applyTranslations(selectedLang);
    });
  });
});
